use crate::errors::TransformError;
use image::DynamicImage;

/// バイト列から画像をデコードする
pub fn decode_image(data: &[u8]) -> Result<DynamicImage, TransformError> {
    image::load_from_memory(data).map_err(|e| {
        TransformError::ProcessingFailed(format!("image decode failed: {e}"))
    })
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_decode_valid_image() {
        // 1x1 白色PNG
        let png_data = vec![
            0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
            0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
            0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, 0x00, 0x00, 0x00,
            0x0C, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0xF8, 0xFF, 0xFF, 0x3F,
            0x00, 0x05, 0xFE, 0x02, 0xFE, 0xDC, 0xCC, 0x59, 0xE7, 0x00, 0x00, 0x00,
            0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
        ];

        let result = decode_image(&png_data);
        assert!(result.is_ok());

        let img = result.unwrap();
        assert_eq!(img.width(), 1);
        assert_eq!(img.height(), 1);
    }

    #[test]
    fn test_decode_invalid_data() {
        let invalid_data = vec![0x00, 0x01, 0x02, 0x03];
        let result = decode_image(&invalid_data);
        assert!(result.is_err());
    }
}
